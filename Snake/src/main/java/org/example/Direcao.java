package org.example;

/**
 * A enumeração Direcao define quatro direções possíveis:
 * UP (cima), DOWN (baixo), LEFT (esquerda) e RIGHT (direita).
 * Esta enumeração pode ser usada, por exemplo, para controlar a direção de movimento de um objeto no jogo.
 */

public enum Direcao {
    UP, DOWN, LEFT, RIGHT
}
